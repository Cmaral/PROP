    public void setInformacion(String titulo, String autor, ArrayList<String> contenido, boolean esConsulta) {
        this.titulo = titulo;
        this.autor = autor;
        this.contenido = contenido;
        this.esConsulta = esConsulta;
        jTextFieldTitulo.setText(titulo);
        jTextFieldAutor.setText(autor);
        for (String s: contenido) {
            jTextAreaContenido.append(s);
            jTextAreaContenido.append("\n");
        }
        
        if (esConsulta) {
            CardLayout card = (CardLayout)Panel.getLayout();
            card.show(Panel, "consulta");
        } else {
            CardLayout card = (CardLayout)Panel.getLayout();
            card.show(Panel, "normal");
        }
    }

